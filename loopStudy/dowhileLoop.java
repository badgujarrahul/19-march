package loopStudy;

public class dowhileLoop {


		public static void main(String[] args) {
			// i want to print 1-10 numbers
			int i = 1;
			do
			{
				System.out.println(i);
				i++;
			}while(i<=10);
			System.out.println("++++++++++++++++");
			
			//I want to print 30 table reverse 
			
			int j=30;
			do
			{
				System.out.println(j);
				j=j-3;
				
			}while (j>=3);
		}

	}
	