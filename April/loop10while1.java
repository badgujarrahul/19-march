package April;

public class loop10while1 {

	public static void main(String[] args) {
		int a = 8;
		while (a <= 482) {
			System.out.println(a);
			a = a + 8;

		}
		System.out.println("**********************");

		int b = 420;
		while (b >= 20) {
			System.out.println(b);
			b = b - 20;

		}
		System.out.println("**********************");

	}

}
